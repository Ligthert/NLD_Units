#define TARGET_LOWER			0x4F	/* DIK_NUMPAD1 - DECREASE CURSOR TARGET HEIGHT */
#define TARGET_SOUTH			0x50	/* DIK_NUMPAD2- MOVE CURSOR TARGET SOUTH */
#define TARGET_WEST				0x4B	/* DIK_NUMPAD4 - MOVE CURSOR TARGET WEST */
