#define TARGET_EAST				0x4D	/* DIK_NUMPAD6 - MOVE CURSOR TARGET EAST */
#define TARGET_RAISE			0x47	/* DIK_NUMPAD7 - INCREASE CURSOR TARGET HEIGHT */
#define TARGET_NORTH			0x48	/* DIK_NUMPAD8- MOVE CURSOR TARGET NORTH */
#define ZOOM_SUBTRACT			0x4A	/* DIK_SUBTRACT - "-" on numeric keypad */ /* DECREASE ZOOM */
#define ZOOM_ADD					0x4E	/* DIK_ADD - "+" on numeric keypad */ /* INCREASE ZOOM */
#define LASER_DESIGNATOR	0x9C	/* DIK_NUMPADENTER - Enter on numeric keypad */ /* LASER DESIGNATOR ON AND OFF */
#define TACTICAL_LASER		0x53	/* DIK_DECIMAL - "." on numeric keypad */ /* TACTICAL LASER ON AND OFF */
#define INSTRUCTIONS			0x24	/* DIK_J - DISPLAY IN-CAMERA INSTRUCTIONS */


