#define RADIUS_DECREASE		0x1E	/* DIK_A - DECREASE RADIUS OF MAV CIRCLE */
#define RADIUS_INCREASE		0x20	/* DIK_D - INCREASE RADIUS OF MAV CIRCLE */
#define CAMERA_EXIT				0x22	/* DIK_G - EXIT CAMERA VIEW */
#define MAP_MARKERADD			0x32	/* DIK_M - ADDS MAP MARKER */
#define VISION_MODE				0x31	/* DIK_N - CHANGES VISION MODE */
#define ALTITUDE_LOWER		0x1F	/* DIK_S - LOWER ALTITUDE */
#define ALTITUDE_RAISE		0x11	/* DIK_W - RAISE ALTITUDE */
